/*First Program in C*/
/* print different types of variables */

#include <stdio.h>
#define PI 3.1416
int main()
{
   int i = 0b1111;
   float f = 3.14;
   char c = 'U';

   printf("\nvalue of i is %i\n", i);
   printf("value of f is %f\n", f);
   printf("value of c is %c\n", c);
   printf("value of PI is %f\n", PI);
   return 0;
}
